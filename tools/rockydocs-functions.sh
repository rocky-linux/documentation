        return 1
    fi
    
    # Check if Docker image exists
    if ! docker image inspect rockydocs-dev >/dev/null 2>&1; then
        print_error "Docker image 'rockydocs-dev' not found"
        print_info "Run setup first: $0 --setup --docker"
        return 1
    fi
    
    cd "$APP_DIR"
    
